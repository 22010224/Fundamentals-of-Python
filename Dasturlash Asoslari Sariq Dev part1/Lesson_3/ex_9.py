#   lstrip() -> matn boshidagi bo'shliqni olib tashlaydi
#   rstrip() -> matn oxiridagi bo'shliqni olib tahslaydi
#   strip() ->  matn boshi va oxiridagi bo'shliqni olib tashlaydi

fruit = '       olma      '
print("I love" + fruit.lstrip())
print("I love" + fruit.rstrip())
print("I love" + fruit.strip())